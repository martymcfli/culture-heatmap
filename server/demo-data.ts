/**
 * Get demo companies for public heat map
 */
export function getDemoCompanies() {
  return DEMO_COMPANIES;
}

/**
 * Get a single demo company by ID
 */
export function getDemoCompanyById(id: number) {
  return DEMO_COMPANIES.find((c) => c.id === id);
}

/**
 * Filter demo companies based on criteria
 */
export function filterDemoCompanies(filters: {
  location?: string;
  industry?: string;
  minScore?: number;
  maxScore?: number;
}) {
  return DEMO_COMPANIES.filter((company) => {
    if (filters.location && !company.headquartersState.includes(filters.location)) {
      return false;
    }
    if (filters.industry && company.industry !== filters.industry) {
      return false;
    }
    if (
      filters.minScore &&
      (company.aggregateScore?.overallRating || 0) < filters.minScore
    ) {
      return false;
    }
    if (
      filters.maxScore &&
      (company.aggregateScore?.overallRating || 0) > filters.maxScore
    ) {
      return false;
    }
    return true;
  });
}
